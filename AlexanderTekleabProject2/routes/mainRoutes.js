const express= require('express');
const meetRoutes = require('./meetRoutes');
const router = express.Router();

router.get('/', (req, res) => {
    res.render('index');
});

router.get('/about', (req, res) => {
    res.render('about');
});

router.get('/contact', (req, res) => {
    res.render('contact');
});

router.use('/connections', meetRoutes);

module.exports = router;